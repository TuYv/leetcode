    class MyStack {
        Queue<Integer> queue;
    public MyStack() {
        queue = new LinkedList<Integer>();
    }
    
    public void push(int x) {
        queue.offer(x);
    }
    
    public int pop() {
        queue.offer(Integer.MAX_VALUE);
        int pre = queue.poll();
        int poll = queue.poll();
        while(poll != Integer.MAX_VALUE) {
            queue.offer(pre);
            pre = poll;
            poll = queue.poll();
        }
        return pre;
    }
    
    public int top() {
        int poll = pop();
        queue.offer(poll);
        return poll;
    }
    
    public boolean empty() {
        return queue.isEmpty();
    }
}

